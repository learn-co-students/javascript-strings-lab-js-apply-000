// Strings
console.log('Hello, everybody!') // a '' String
console.log("Hello, everybody!") // a "" String
var specialGuest = "Neil deGrasse Tyson"
// Concatenation
console.log("Hello, " + specialGuest + "!")  // "Hello, Neil deGrasse Tyson" - concatenation
console.log(`Hello, ${specialGuest}! High ${3 + 2}!`)  // Hello, Neil deGrasse Tyson! High 5!

